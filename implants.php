         <div class="faq-content-4">
          <p class="faq-paragraph-4">
           Good candidates for dental implants are generally in good overall health and have enough jawbone to support the implant. Patients with periodontal disease or other medical conditions may need additional treatment before undergoing implant surgery.
          </p>
         </div>
        </div>
        <div class="faq-question-wrap">
         <a class="faq-question-bar-3 w-inline-block" href="#">
          <h3 class="h3 question-title-4">
           How long do dental implants last?
          </h3>
          <img alt="Blue Plus logo " class="image-9" src="./resources/cdn.prod.website-files.com/6230bfc886c8142108c44052/6230bfc886c8148790c440ce_close-fill.svg"/>
         </a>
         <div class="faq-content-4">
          <p class="faq-paragraph-4">
           Dental implants can last for many years with proper care and maintenance. In fact, they are often considered a long-term solution for dental restoration. The success rate of dental implants is very high, with studies showing that up to 98% of implants remain in place after 10 years.
           <br/>
          </p>
         </div>
        </div>
        <div class="faq-question-wrap">
         <a class="faq-question-bar-3 w-inline-block" href="#">
          <h3 class="h3 question-title-4">
           Is the implant surgery painful?
           <br/>
          </h3>
          <img alt="Blue Plus logo " class="image-9" src="./resources/cdn.prod.website-files.com/6230bfc886c8142108c44052/6230bfc886c8148790c440ce_close-fill.svg"/>
         </a>
         <div class="faq-content-4">
          <p class="faq-paragraph-4">
           Dental implant surgery is typically performed under local anesthesia and is not considered painful. Patients may experience some discomfort or swelling after the procedure, but this can usually be managed with over-the-counter pain medication.
           <br/>
          </p>
         </div>
        </div>
