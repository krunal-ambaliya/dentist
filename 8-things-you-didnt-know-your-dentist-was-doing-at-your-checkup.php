         <h2>
          5. What To Do After Your Dental Checkup
         </h2>
         <p>
          Dental implants are a popular way to replace missing teeth. They are made of titanium and look like screws. The implant is placed in the jawbone, and a tooth restoration is attached to the top. There are several types of dental implants:
         </p>
         <p>
          <br/>
         </p>
         <p>
          • Endosteal implants: These are the most common type of implant. They are inserted into the jawbone with a surgical tool.
         </p>
         <p>
          <br/>
         </p>
         <p>
          • Subperiosteal implants: These implants are placed on top of the jawbone under the gum line.
         </p>
         <p>
          <br/>
         </p>
         <p>
          • Platelet-rich plasma (PRP) implants: PRP is a substance made from your own blood that is used to promote healing. It is injected into the jawbone around the implant.
         </p>
         <p>
          <br/>
         </p>
         <p>
          There are several factors that will determine which type of implant is best for you, including your dental history, health, and budget. Talk to your dentist about which type of implant is right for you.
         </p>
