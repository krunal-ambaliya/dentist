           <div class="article-faq-inner-wrapper" id="w-node-_30d097a9-b48c-1104-ea82-61593fafdc93-c3d11829">
            <div class="paragraph-regular-bold margin-bottom-12 is-articles">
             1. What are the Risks Associated with Teeth Extraction?
             <br/>
            </div>
            <p class="paragraph-regular">
             Like any surgical procedure, there are risks associated with teeth extraction. Some of the most common risks include:
             <br/>
             - Infection
             <br/>
             - Bleeding
             <br/>
             - Damage to adjacent teeth
             <br/>
             - Nerve damage
             <br/>
             - Jaw fracture
             <br/>
             - Sinus problems
             <br/>
            </p>
           </div>
           <div class="article-faq-inner-wrapper" id="w-node-_057738a1-a40e-77a4-2c18-d7a96834ede6-c3d11829">
            <div class="paragraph-regular-bold margin-bottom-12 is-articles">
             2. How do I Prepare for Teeth Extraction?
             <br/>
            </div>
            <p class="paragraph-regular">
             Your dentist will give you specific instructions on how to prepare for your procedure, depending on the type of extraction being performed. In general, you should:
             <br/>
             - Brush and floss your teeth thoroughly
             <br/>
             - Avoid smoking
             <br/>
             - Eat a light meal before the procedure
             <br/>
             - Wear loose-fitting clothing to the appointment
             <br/>
             - Bring a list of any medications you are taking
             <br/>
            </p>
           </div>
           <div class="article-faq-inner-wrapper" id="w-node-_408cb1c6-cbaa-f1ec-34ed-09a1cd30e63a-c3d11829">
            <div class="paragraph-regular-bold margin-bottom-12 is-articles">
             3. What is the Recovery Time for Teeth Extraction?
             <br/>
            </div>
            <p class="paragraph-regular">
             Recovery time after a teeth extraction can vary from person to person. Most people experience some pain and swelling for a few days after the procedure. You should avoid vigorous activity and smoking during this time to promote healing. Your dentist will give you specific instructions on how to care for your mouth after the procedure.
             <br/>
            </p>
           </div>
           <div class="article-faq-inner-wrapper" id="w-node-f75f7ded-62ea-20d3-697a-818508d5269e-c3d11829">
            <div class="paragraph-regular-bold margin-bottom-12 is-articles">
             4. Are There Any Complications Associated with Teeth Extraction?
             <br/>
            </div>
            <p class="paragraph-regular">
             Complications from teeth extraction are rare, but can occur. Some of the most common complications include:
             <br/>
             - Infection
             <br/>
             - Bleeding
             <br/>
             - Damage to adjacent teeth
             <br/>
             - Nerve damage
             <br/>
             - Jaw fracture
             <br/>
             - Sinus problems
             <br/>
             If you experience any of these complications, it is important to contact your dentist or doctor immediately. Teeth extractions are generally safe procedures with a low risk of complication. However, as with any surgery, there are always risks involved. It is important to discuss these risks with your dentist or oral surgeon prior to having the procedure performed.
             <br/>
            </p>
           </div>
           <div class="article-faq-inner-wrapper" id="w-node-_9ea4ff96-4e33-2ce0-9b99-6e7dd7eb4126-c3d11829">
            <div class="paragraph-regular-bold margin-bottom-12 is-articles">
             5. What Can I Expect During A Teeth Extraction?
             <br/>
            </div>
            <p class="paragraph-regular">
             Teeth extraction is usually performed under local anesthesia, which numbs the area around the tooth. You may also be given sedatives to help you relax during the procedure. The dentist will make an incision in your gums and then remove the tooth using forceps. Once the tooth is removed, the area will be cleaned and sutured closed. You can expect some pain and swelling after the procedure, which can be controlled with pain medication. Most people recover from a teeth extraction within a few days.
            </p>
