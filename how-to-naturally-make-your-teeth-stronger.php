            <div class="paragraph-regular-bold margin-bottom-12 is-articles">
             1. How long will I need to wear braces?
             <br/>
            </div>
            <p class="paragraph-regular">
             The amount of time you need to wear braces will vary depending on the severity of your problem and the type of braces you choose. In general, most people need to wear braces for one to two years.
             <br/>
            </p>
           </div>
           <div class="article-faq-inner-wrapper" id="w-node-_057738a1-a40e-77a4-2c18-d7a96834ede6-c3d117c4">
            <div class="paragraph-regular-bold margin-bottom-12 is-articles">
             2. How often will I need to see the orthodontist?
             <br/>
            </div>
            <p class="paragraph-regular">
             You will need to see the orthodontist every four to eight weeks so that they can check on your progress and make any necessary adjustments.
             <br/>
            </p>
           </div>
           <div class="article-faq-inner-wrapper" id="w-node-_408cb1c6-cbaa-f1ec-34ed-09a1cd30e63a-c3d117c4">
            <div class="paragraph-regular-bold margin-bottom-12 is-articles">
             3. How do I care for my braces?
             <br/>
            </div>
            <p class="paragraph-regular">
             It is important to brush and floss your teeth regularly and to avoid eating hard or sticky foods. You should also be careful when playing sports or doing other activities so that you don’t damage your braces.
             <br/>
            </p>
           </div>
           <div class="article-faq-inner-wrapper" id="w-node-f75f7ded-62ea-20d3-697a-818508d5269e-c3d117c4">
            <div class="paragraph-regular-bold margin-bottom-12 is-articles">
             4. Will I need to wear a retainer after my braces are removed?
             <br/>
            </div>
            <p class="paragraph-regular">
             In most cases, yes. Wearing a retainer helps ensure that your teeth stay in their new alignment.
             <br/>
            </p>
