        <h1 class="h1">
         7 Things You Didn't Know About Dental Crowns
         <br/>
        </h1>
        <div class="subtext-large is-articles">
         <br/>
         Dental crowns are common dental procedures that many people undergo every year. Crowns are used to restore a tooth’s function and appearance after a root canal, or when a large cavity threatens the structural integrity of the tooth. Dental crowns can also be used to improve the aesthetics of your smile.
         <br/>
         ‍
         <br/>
         While most people know the basics of what dental crowns are and what they do, there are a few things that many people don't know about dental crowns. Here are seven of them.
         <br/>
        </div>
