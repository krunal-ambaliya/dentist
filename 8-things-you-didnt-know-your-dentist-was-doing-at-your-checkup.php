        <h1 class="h1">
         8 Things You Didn’t Know Your Dentist Was Doing At Your Checkup
         <br/>
        </h1>
        <div class="subtext-large is-articles">
         <br/>
         Most people know that a dental checkup is important for keeping your teeth and gums healthy. But what many people don't realize is that there's more to a dental checkup than just a cleaning. Dentists also check for cavities, gum disease and other oral health problems during your checkup. They may also take x-rays or other diagnostic measures to get a better view of what's going on inside your mouth. So if you're overdue for a checkup, make an appointment today! You'll be glad you did.
         <br/>
         ‍
         <br/>
         1. Checking for cavities: One of the most important things your dentist will do during your checkup is check for cavities. Cavities are small holes in your teeth that can be caused by tooth decay. If left untreated, cavities can cause pain and other problems.
         <br/>
         ‍
         <br/>
         2. Checking for gum disease: Gum disease is an infection of the gums that can lead to tooth loss if left untreated. During your checkup, your dentist will check for signs of gum disease, such as bleeding gums or redness and swelling of the gums.
         <br/>
         ‍
         <br/>
         3. Checking for oral cancer: Oral cancer is a serious condition that can be deadly if not treated early. Your dentist will look for signs of oral cancer during your checkup, such as lumps or bumps in the mouth, changes in the color of the tissue in the mouth, or sores that don't heal.
         <br/>
         ‍
         <br/>
         4. Checking for other oral health problems: In addition to cavities, gum disease and oral cancer, your dentist will also look for other oral health problems during your checkup. This can include anything from a toothache to bad breath.
         <br/>
         ‍
         <br/>
         5. Taking x-rays: X-rays are an important part of a dental checkup. They allow your dentist to see what's going on inside your teeth and gums and can help detect problems early.
         <br/>
         ‍
         <br/>
         6. Cleaning your teeth: A professional cleaning is an important part of a dental checkup. During a cleaning, your dentist or dental hygienist will remove plaque and tartar from your teeth. They will also polish your teeth and floss them.
         <br/>
         ‍
         <br/>
         7. Examining your mouth: In addition to taking x-rays and cleaning your teeth, your dentist will also take a close look at your mouth during your checkup. This includes looking at your tongue, gums, teeth, and the roof of your mouth.
         <br/>
         ‍
         <br/>
         8. Making sure you're comfortable: Your dentist wants you to be as comfortable as possible during your checkup. They will make sure you're relaxed and comfortable before they start any procedures. If you have any questions or concerns, don't hesitate to ask!
         <br/>
